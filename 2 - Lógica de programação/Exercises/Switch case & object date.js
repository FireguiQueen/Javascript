function sayWeekDay(day){
    switch(day){
        case 0: 
            return 'Domingo';
            break;
        
        case 1: 
            return 'Segunda-feira';
            break;
        
        case 0: 
            return 'Domingo';
            break;
        
        case 0: 
            return 'Domingo';
            break;
        
        case 0: 
            return 'Domingo';
            break;
        
        case 0: 
            return 'Domingo';
            break;
        

    }
}